# TAB 3 — Scenario Simulator
# ══════════════════════════════════════════════════════════════════════════════
with tab3:
    st.subheader("🔬 Community Scenario Simulator")
    st.markdown(
        "**What if** primary care attachment improves in a specific community? "
        "This tool re-calculates vulnerability and patient priority scores to estimate impact."
    )

    # Community selector — only communities with patients
    patient_communities = (
        comm_scores[comm_scores["chsa_code"].isin(priorities["chsa_code"].unique())]
        .sort_values("vulnerability_score", ascending=False)
    )
    community_options = {
        f"{r['chsa_name']}  (vuln: {r['vulnerability_score']:.0f}, "
        f"unattached: {r['pct_without_family_doctor']:.1f}%)": r["chsa_code"]
        for _, r in patient_communities.iterrows()
    }

    sc1, sc2 = st.columns([2, 1])
    with sc1:
        selected_label = st.selectbox("Select community", options=list(community_options.keys()))
    selected_chsa = community_options[selected_label]

    with sc2:
        max_unattached = float(
            comm_scores.loc[comm_scores["chsa_code"] == selected_chsa,
                            "pct_without_family_doctor"].iloc[0]
        )
        improvement = st.slider(
            "Improve primary care attachment by (pp)",
            min_value=0.0, max_value=min(max_unattached, 25.0),
            value=min(5.0, max_unattached), step=1.0,
            help="Percentage points reduction in '% without family doctor'"
        )

    # Run scenario
    scenario = simulate_community_scenario(priorities, comm_scores, selected_chsa, improvement)

    # ── Results ───────────────────────────────────────────────────────────────
    st.markdown("---")
    r1, r2, r3 = st.columns(3)

    with r1:
        st.markdown("### Current State")
        st.metric("Unattached rate", f"{scenario['current_unattached']}%")
        st.metric("Vulnerability score", f"{scenario['current_vuln']}/100")
        st.metric("Patients in community", scenario["n_patients"])
        # Before tier breakdown
        st.markdown("**Priority tier breakdown**")
        for t in TIER_ORDER:
            count = scenario["before_tiers"].get(t, 0)
            if count:
                st.markdown(f"- {t}: **{count}**")

    with r2:
        st.markdown("### After Scenario")
        st.metric("Unattached rate",
                  f"{scenario['new_unattached']}%",
                  delta=f"-{improvement:.0f}pp",
                  delta_color="inverse")
        st.metric("Vulnerability score",
                  f"{scenario['new_vuln']:.1f}/100",
                  delta=f"{scenario['new_vuln'] - scenario['current_vuln']:.1f}",
                  delta_color="inverse")
        st.metric("Patients in community", scenario["n_patients"])
        st.markdown("**Priority tier breakdown**")
        for t in TIER_ORDER:
            before = scenario["before_tiers"].get(t, 0)
            after  = scenario["after_tiers"].get(t, 0)
            diff   = after - before
            if before or after:
                arrow = f" ({diff:+d})" if diff != 0 else ""
                st.markdown(f"- {t}: **{after}**{arrow}")

    with r3:
        st.markdown("### Projected Impact")
        st.metric("Patients improved tier", scenario["n_improved"],
                  help="Number of patients who dropped to a lower-urgency tier")
        st.metric("Fewer high-priority patients", scenario["avoided_hp"],
                  help="Patients who moved out of CRITICAL/HIGH")
        st.metric("Est. avoided ED visits/yr", scenario["avoided_ed_visits"])
        st.metric("Est. cost savings/yr", f"${scenario['avoided_cost']:,.0f} CAD")

    # ── Before/After comparison chart ─────────────────────────────────────────
    st.markdown("---")
    chart_data = []
    for t in TIER_ORDER:
        chart_data.append({"Tier": t, "State": "Before", "Patients": scenario["before_tiers"].get(t, 0)})
        chart_data.append({"Tier": t, "State": "After",  "Patients": scenario["after_tiers"].get(t, 0)})
    chart_df = pd.DataFrame(chart_data)

    fig_compare = px.bar(
        chart_df, x="Tier", y="Patients", color="State",
        barmode="group",
        color_discrete_map={"Before": "#ef5350", "After": "#66bb6a"},
        title=f"Priority Tier Shift — {scenario['community_name']}",
        height=320,
    )
    fig_compare.update_layout(margin=dict(l=0, r=0, t=40, b=0))
    st.plotly_chart(fig_compare, use_container_width=True)

    # Narrative
    st.info(
        f"**Planning estimate:** Reducing the unattached rate in **{scenario['community_name']}** "
        f"from {scenario['current_unattached']}% to {scenario['new_unattached']}% would lower "
        f"the community vulnerability score from {scenario['current_vuln']} to {scenario['new_vuln']:.1f}, "
        f"moving **{scenario['n_improved']}** patient(s) to lower-urgency tiers and potentially "
        f"avoiding **{scenario['avoided_ed_visits']}** ED visits/year "
        f"(~${scenario['avoided_cost']:,.0f} CAD)."
    )
    st.caption(
        "⚠️ This is a sensitivity analysis within the scoring framework, not a causal prediction. "
        "Community-level improvements affect the contextual modifier only; "
        "individual patient clinical risk scores remain unchanged."
    )


