        fig_scatter.update_traces(textposition="top center")
        fig_scatter.update_layout(margin=dict(l=0, r=0, t=10, b=0))
        st.plotly_chart(fig_scatter, use_container_width=True)

        # BC Provincial Context
        st.markdown("---")
        st.subheader("🏔️ BC Provincial Context")

        st.markdown("**Opioid Crisis Trend** — deaths per 100k")
        fig_opioid = px.area(
            opioid_trend, x="year", y="rate_per_100k", markers=True,
            labels={"rate_per_100k": "Deaths / 100k", "year": ""},
            color_discrete_sequence=["#d62728"], height=240,
        )
        fig_opioid.add_vline(x=2020, line_dash="dash", line_color="gray",
                             annotation_text="COVID-19", annotation_position="top right")
        fig_opioid.update_layout(margin=dict(l=0, r=0, t=10, b=0))
