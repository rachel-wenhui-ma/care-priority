# ── Methodology note ──────────────────────────────────────────────────────────
with st.expander("📐 **About this model — methodology, linkage, and limitations**"):
    st.markdown("""
### What this tool does

This dashboard is a **prioritization framework for care coordination outreach** —
it ranks patients by how urgently they may benefit from proactive follow-up.
It is **not** a diagnostic tool, a causal model, or a validated clinical risk predictor.

### How the two scores are computed

| Score | Source | Level | Method |
|-------|--------|-------|--------|
| **Patient risk** (0–100) | Track 1: encounters, labs, medications | Individual | Rule-based: ED frequency, chronic disease control, polypharmacy, AMA history, follow-up gap, wait burden, language barrier |
| **Community vulnerability** (0–100) | Track 2: BC community health indicators | Area (CHSA) | Weighted composite of 6 normalized indicators (% without family doctor, poverty rate, opioid overdose rate, chronic disease burden, mental health hospitalization, ER visit rate) |

The two scores are **computed independently**, then combined as:

> `combined_score = 0.60 × patient_risk + 0.40 × community_vulnerability`

The weights are explicit and adjustable in `config.py`.

### How patients are linked to communities

Patient postal codes (Forward Sortation Area / first 3 characters) are mapped to
BC Community Health Service Areas (CHSAs) via geographic approximation.
This follows the **area-based linkage** approach commonly used in Canadian health
inequality analysis (CIHI equity stratification via PCCF/PCCF+).

### What the community score does and does not mean

- ✅ **Does mean:** This patient lives in a community where structural barriers to
  primary care access are more prevalent.
- ❌ **Does not mean:** This specific patient has no family doctor, or that their
  community environment *caused* their clinical risk.

The community score is a **contextual modifier**, not an individual-level fact.

### Key limitations

1. **Ecological fallacy:** Area-level statistics are used as individual-level context.
2. **No causal validation:** Community-level access barriers are not proven to cause repeat ED use in this dataset.
3. **Synthetic data:** All Track 1 patient data is generated by Synthea.
4. **FSA → CHSA mapping is approximate:** Based on known BC postal code geography.
5. **Intervention estimates are modelled, not observed:** The 30% ED visit reduction is an assumption.
6. **Scenario projections are sensitivity analyses**, not predictions — they show how the scoring framework responds to input changes.

### Intended use

Designed for **care coordinators and planning teams** to support outreach prioritization
and community-level resource planning. Use alongside clinical judgement.
""")

