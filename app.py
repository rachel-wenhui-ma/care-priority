# ══════════════════════════════════════════════════════════════════════════════
# TAB 1 — Priority Queue
# ══════════════════════════════════════════════════════════════════════════════
with tab1:
    col_left, col_right = st.columns([3, 2])

    with col_right:
        # Tier donut
        tier_counts = filtered["priority_tier"].value_counts().reindex(TIER_ORDER, fill_value=0)
        fig_donut = go.Figure(go.Pie(
            labels=tier_counts.index, values=tier_counts.values,
            hole=0.55,
            marker_colors=[TIER_COLORS[t] for t in tier_counts.index],
            textinfo="label+value",
        ))
        fig_donut.update_layout(title="Priority Tier Breakdown",
                                showlegend=False, height=260,
                                margin=dict(l=0, r=0, t=40, b=0))
        st.plotly_chart(fig_donut, use_container_width=True)

        # Top risk flags
        st.markdown("**Most Common Risk Flags**")
        all_flags = [f for flags in filtered["risk_flags"] for f in flags]
        if all_flags:
            flag_series = pd.Series(all_flags).value_counts().head(6)
            fig_flags = px.bar(
                x=flag_series.values, y=flag_series.index, orientation="h",
                labels={"x": "Patients", "y": ""},
                color=flag_series.values, color_continuous_scale="Oranges",
                height=240,
            )
            fig_flags.update_layout(margin=dict(l=0, r=0, t=0, b=0),
                                     coloraxis_showscale=False)
            st.plotly_chart(fig_flags, use_container_width=True)

        # Intervention planner results
        st.markdown("---")
        st.markdown(f"**🎯 Outreach top {n_outreach} patients**")
        sim = simulate_intervention(priorities, n_outreach)
        s1, s2 = st.columns(2)
        s1.metric("Avoided ED visits/yr", sim["avoided_ed_visits"])
        s2.metric("Cost savings/yr", f"${sim['avoided_cost_cad']:,.0f}")
        if sim["key_conditions"]:
            for cond in sim["key_conditions"]:
                st.caption(f"• {cond}")

    with col_left:
        st.subheader(f"Priority Queue  ({len(filtered):,} patients)")
        if filtered.empty:
            st.info("No patients match the current filters.")
        else:
            page_size = 20
            total_pages = max(1, (len(filtered) - 1) // page_size + 1)
            page = st.number_input("Page", 1, total_pages, 1, 1)
            page_df = filtered.iloc[(page - 1) * page_size : page * page_size]

            for _, row in page_df.iterrows():
                tier  = row["priority_tier"]
                emoji = row["priority_emoji"]

                with st.expander(
                    f"{emoji} **{tier}** · {row['patient_id']} · "
                    f"Age {int(row['age'])} {row['sex']} · "
                    f"{row['chsa_name']} · Score {row['combined_score']:.0f}/100"
                ):
                    c_a, c_b, c_c = st.columns(3)
                    c_a.metric("Patient Risk",    f"{row['risk_score']:.0f}/100")
                    c_b.metric("Community Vuln.", f"{row['vulnerability_score']:.0f}/100")
                    c_c.metric("Combined Score",  f"{row['combined_score']:.0f}/100")

                    # "Why now?" structured reasoning
                    flags   = row.get("risk_flags", [])
                    cats    = categorize_flags(flags)
                    impacts = compute_system_impact(row)

                    why1, why2, why3 = st.columns(3)
                    with why1:
                        st.markdown("**⚕️ Clinical Need**")
                        for f in (cats["clinical_need"] or ["_No acute clinical flags_"]):
                            st.markdown(f"- {f}")
                    with why2:
                        st.markdown("**🚧 Access Barrier**")
                        barriers = cats["access_barrier"].copy()
                        gp_pct = row.get("pct_without_family_doctor", 0)
                        if gp_pct >= 15 and not any("GP" in b or "family" in b.lower() for b in barriers):
                            barriers.insert(0, f"{gp_pct:.0f}% in community lack a family doctor")
                        for f in (barriers or ["_No significant access barriers_"]):
                            st.markdown(f"- {f}")
                    with why3:
                        st.markdown("**📊 System Impact**")
                        for imp in impacts:
                            st.markdown(f"- {imp}")

                    st.info(f"**Recommended action:** {row['priority_action']}")

                    if st.button("💬 Generate AI rationale", key=f"btn_{row['patient_id']}"):
                        with st.spinner("Generating explanation…"):
                            explanation = generate_explanation(row.to_dict())
                        st.success(explanation)


