# ── Data loading (cached) ─────────────────────────────────────────────────────
@st.cache_data(show_spinner="Loading and scoring patient data…")
def build_data():
    data               = load_all()
    scored             = score_patients(data["master"])
    comm_sc            = score_communities(data["community"])
    priorities, tholds = compute_priorities(scored, comm_sc)
    stats              = summary_stats(priorities)
    outcomes           = cohort_outcomes(priorities)
    return (priorities, comm_sc, stats, tholds, outcomes,
            data["opioid_trend"], data["wait_times"], data["bc_wait"])

(priorities, comm_scores, stats, thresholds, outcomes,
 opioid_trend, wait_times, bc_wait) = build_data()

