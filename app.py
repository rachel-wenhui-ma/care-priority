# ── Sidebar ───────────────────────────────────────────────────────────────────
with st.sidebar:
    st.header("Filters")
    selected_tiers = st.multiselect(
        "Priority tier", options=TIER_ORDER, default=["CRITICAL", "HIGH"])
    selected_communities = st.multiselect(
        "Community",
        options=sorted(priorities["chsa_name"].dropna().unique()),
        default=[], placeholder="All communities")
    min_risk = st.slider("Minimum patient risk score", 0, 100, 0)

    st.divider()
    st.caption("**Score weights:** Patient risk 60% · Community vuln. 40%")

    st.divider()
    st.subheader("🎯 Intervention Planner")
    st.caption("*If your team can outreach N patients this week…*")
    n_outreach = st.slider("Outreach capacity", 10, 200, 50, 10)

# ── Apply filters ─────────────────────────────────────────────────────────────
filtered = priorities.copy()
if selected_tiers:
    filtered = filtered[filtered["priority_tier"].isin(selected_tiers)]
if selected_communities:
    filtered = filtered[filtered["chsa_name"].isin(selected_communities)]
filtered = filtered[filtered["risk_score"] >= min_risk]


